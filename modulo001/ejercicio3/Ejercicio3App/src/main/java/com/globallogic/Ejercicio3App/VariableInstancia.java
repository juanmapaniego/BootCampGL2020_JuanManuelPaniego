package com.globallogic.Ejercicio3App;

public class VariableInstancia {
	private Integer engPoints;
	private Integer mathsPoints;
	private Integer phyPoints;
	
	public Integer getEngPoints() {
		return engPoints;
	}
	public void setEngPoints(Integer engPoints) {
		this.engPoints = engPoints;
	}
	public Integer getMathsPoints() {
		return mathsPoints;
	}
	public void setMathsPoints(Integer mathsPoints) {
		this.mathsPoints = mathsPoints;
	}
	public Integer getPhyPoints() {
		return phyPoints;
	}
	public void setPhyPoints(Integer phyPoints) {
		this.phyPoints = phyPoints;
	}
	
	
}

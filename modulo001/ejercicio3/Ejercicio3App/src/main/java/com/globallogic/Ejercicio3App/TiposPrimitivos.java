public class TiposPrimitivos {

	public static void main(String[] args) {
		int myNum = 15;
		System.out.println("El valor de mi numero es " + myNum);
		char myChar = 'a';
		System.out.println("El valor de mi caracter es " + myChar);
		boolean myBoolean = true;
		System.out.println("El valor de mi boolean es " + myBoolean);
		float myFloat = 2436.2456f;
		System.out.println("El valor de mifloat es " + myFloat);
		short myShort = 500;
		System.out.println("El valor de mishort es " + myShort);
	}

}
